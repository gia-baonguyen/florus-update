func AutoMigrate(db *gorm.DB) error {
	log.Println("Running auto-migration...")

	// Migrate each model separately to ensure all tables are created
	// even if some models have constraint errors
	modelsToMigrate := []interface{}{
		&models.User{},
		&models.Category{},
		&models.Product{},
		&models.ProductTag{},
		&models.ProductIngredient{},
		&models.ProductRelationship{},
		&models.ProductImage{},
		&models.CartItem{},
		&models.Order{},
		&models.OrderItem{},
		&models.WishlistItem{},
		&models.Review{},
		&models.ReviewImage{},
		&models.Coupon{},
		&models.UserAddress{},
		&models.ShippingZone{},
		&models.ShippingMethod{},
		&models.ShippingZoneMethod{},
		&models.LoyaltyTransaction{},
		&models.Return{},
		&models.ReturnItem{},
	}

	var hasErrors bool
	for _, model := range modelsToMigrate {
		err := db.AutoMigrate(model)
		if err != nil {
			// Ignore Oracle errors for existing objects
			// ORA-00955: name already used by an existing object (index exists)
			// ORA-02261: unique or primary key already exists
			// ORA-01430: column being added already exists
			if strings.Contains(err.Error(), "ORA-00955") ||
				strings.Contains(err.Error(), "ORA-02261") ||
				strings.Contains(err.Error(), "ORA-01430") {
				// Silently continue - object already exists
				continue
			}
			log.Printf("Migration warning for %T: %v", model, err)
			hasErrors = true
		}
	}

	// Create core business tables that may fail with GORM+Oracle automigration
	// Products table is critical for most user flows, so ensure it's present.
	createProductsTable(db)
	createProductTagsTable(db)
	createProductIngredientsTable(db)
	createProductRelationshipsTable(db)
	createProductImagesTable(db)
	createCartItemsTable(db)
	createWishlistItemsTable(db)

	// Ensure orders core tables exist before payments (which has FK to orders)
	createOrdersTable(db)
	createOrderItemsTable(db)
	ensureOrderExtraColumns(db)

	// Create payments table manually due to Oracle constraint issues with related models
	createPaymentsTable(db)

	// Create user_events table manually
	createUserEventsTable(db)

	// Ensure loyalty-related columns exist on users
	ensureUserLoyaltyColumns(db)

	// Create reviews table manually due to Oracle constraint issues
	createReviewsTable(db)
	createReviewImagesTable(db)

	if hasErrors {
		log.Println("Migration completed with some warnings")
	} else {
		log.Println("Migration completed successfully")
	}
	return nil
}

func createPaymentsTable(db *gorm.DB) {
	// Check if payments table exists
	var count int64
	db.Raw("SELECT COUNT(*) FROM USER_TABLES WHERE TABLE_NAME = 'PAYMENTS'").Scan(&count)
	if count > 0 {
		return // Table already exists
	}

	log.Println("Creating payments table...")
	sql := `
		CREATE TABLE payments (
			ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
			ORDER_ID INTEGER NOT NULL,
			TRANSACTION_ID VARCHAR2(100),
			METHOD VARCHAR2(20) NOT NULL,
			AMOUNT NUMBER(15,2) NOT NULL,
			STATUS VARCHAR2(20) DEFAULT 'pending',
			PROVIDER_TRANS_ID VARCHAR2(100),
			PAYMENT_URL VARCHAR2(1000),
			CALLBACK_DATA CLOB,
			ERROR_MESSAGE VARCHAR2(500),
			PAID_AT TIMESTAMP WITH TIME ZONE,
			CREATED_AT TIMESTAMP WITH TIME ZONE,
			UPDATED_AT TIMESTAMP WITH TIME ZONE,
			CONSTRAINT FK_PAYMENTS_ORDER FOREIGN KEY (ORDER_ID) REFERENCES orders(ID)
		)
	`
	if err := db.Exec(sql).Error; err != nil {
		if !strings.Contains(err.Error(), "ORA-00955") {
			log.Printf("Error creating payments table: %v", err)
		}
	} else {
		log.Println("Payments table created successfully")
	}

	// Create unique index on transaction_id
	db.Exec("CREATE UNIQUE INDEX IDX_PAYMENTS_TRANSACTION_ID ON payments(TRANSACTION_ID)")
	// Create index on order_id
	db.Exec("CREATE INDEX IDX_PAYMENTS_ORDER_ID ON payments(ORDER_ID)")
}

