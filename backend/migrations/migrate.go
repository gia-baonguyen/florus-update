func createReviewsTable(db *gorm.DB) {
	// Check if reviews table exists
	var count int64
	db.Raw("SELECT COUNT(*) FROM USER_TABLES WHERE TABLE_NAME = 'REVIEWS'").Scan(&count)
	if count == 0 {
		log.Println("Creating reviews table...")
		sql := `
			CREATE TABLE reviews (
				ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
				USER_ID INTEGER NOT NULL,
				PRODUCT_ID INTEGER NOT NULL,
				RATING INTEGER NOT NULL,
				COMMENT_TEXT CLOB,
				CREATED_AT TIMESTAMP WITH TIME ZONE,
				UPDATED_AT TIMESTAMP WITH TIME ZONE,
				CONSTRAINT FK_REVIEWS_USER FOREIGN KEY (USER_ID) REFERENCES users(ID),
				CONSTRAINT FK_REVIEWS_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES products(ID),
				CONSTRAINT UQ_REVIEWS_USER_PRODUCT UNIQUE (USER_ID, PRODUCT_ID)
			)
		`
		if err := db.Exec(sql).Error; err != nil {
			if !strings.Contains(err.Error(), "ORA-00955") {
				log.Printf("Error creating reviews table: %v", err)
			}
		} else {
			log.Println("Reviews table created successfully")
		}
	} else {
		// Table exists: ensure COMMENT_TEXT column exists (older schema may have different layout)
		var colCount int64
		db.Raw("SELECT COUNT(*) FROM USER_TAB_COLUMNS WHERE TABLE_NAME = 'REVIEWS' AND COLUMN_NAME = 'COMMENT_TEXT'").Scan(&colCount)
		if colCount == 0 {
			log.Println("Adding COMMENT_TEXT column to reviews table...")
			if err := db.Exec("ALTER TABLE reviews ADD COMMENT_TEXT CLOB").Error; err != nil && !strings.Contains(err.Error(), "ORA-01430") {
				log.Printf("Error adding COMMENT_TEXT column: %v", err)
			}
		}
	}

	// Create indexes
	db.Exec("CREATE INDEX IDX_REVIEWS_USER_ID ON reviews(USER_ID)")
	db.Exec("CREATE INDEX IDX_REVIEWS_PRODUCT_ID ON reviews(PRODUCT_ID)")
}

